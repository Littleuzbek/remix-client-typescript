function translateText() {
const isBrowser = typeof window !== "undefined";
  if(!isBrowser){
    return null
  }
  const userLanguage = localStorage?.getItem("exkoLang");

  if (userLanguage === "UZ") {
    return uzbekLang;
  } else if (userLanguage === "RU") {
    return russianLang;
  } else if (userLanguage === "KR") {
    return koreanLang;
  } else {
    return uzbekLang;
  }
}

function editLanguage(event: string) {
  localStorage.setItem("exkoLang", event);
  window.location.reload();
}

export { translateText, editLanguage };
